#include "ft_printf.h"

void	ft_putnbr(int n, int *count)
{
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		*count = 11;
		return ;
	}
	else if (n < 0)
	{
		write(1, "-", 1);
		*count = 1;
		n *= -1;
	}
	if (n >= 10)
		ft_putnbr(n / 10, count);
	*count += ft_putchar(n % 10 + '0');
}
